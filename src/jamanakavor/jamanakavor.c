int		ft_strcmp_space(char *s1, char *s2)
{
	int i;

	i = 0;
	while (s1[i] != '\0' && s2[i] != '\0' && s1[i] != ' ' && s2[i] != ' ' && s1[i] == s2[i])
	{
		i++;
	}
	if(s1[i] == ' ')
		i--;
	return (s1[i] - s2[i]);
}
